import torch
import torch.nn.functional as F

from core.SSD.anchor import DefaultBoxes


class MultiBoxLoss:
    def __init__(self, cfg):
        self.device = cfg["device"]
        # torch.Tensor, shape: (先验框总数(8732), 4)
        self.default_boxes = torch.from_numpy(DefaultBoxes(cfg).__call__()).to(self.device)
        self.num_classes = cfg["Model"]["num_classes"] + 1
        self.threshold = cfg["Loss"]["overlap_thresh"]
        self.variance = cfg["Loss"]["variance"]
        self.negpos_ratio = cfg["Loss"]["neg_pos"]

    def __call__(self, y_true, y_pred):
        """

        Args:
            y_true: torch.Tensor, shape: (batch_size, max_num_boxes, 5(xmin, ymin, xmax, ymax, class_index))
            y_pred: (loc, conf), 其中loc的shape是(batch_size, 先验框总数, 4), conf的shape是(batch_size, 先验框总数, self.num_classes)

        Returns:

        """
        loc_data, conf_data = y_pred
        batch_size = loc_data.size()[0]
        priors = self.default_boxes[:loc_data.size()[1], :]
        num_priors = (priors.size()[0])
        num_classes = self.num_classes

        # 匹配先验框和GT boxes
        loc_t = torch.Tensor(batch_size, num_priors, 4)
        conf_t = torch.LongTensor(batch_size, num_priors)
        for idx in range(batch_size):
            # 去除y_true中padding的检测框
            true_boxes = y_true[idx]
            true_boxes = true_boxes[true_boxes[:, -1] != -1]
            truths = true_boxes[:, :-1].data
            labels = true_boxes[:, -1].data
            defaults = priors.data
            match(self.threshold, truths, defaults, self.variance, labels,
                  loc_t, conf_t, idx)

        loc_t = loc_t.to(self.device)
        conf_t = conf_t.to(self.device)
        loc_t.requires_grad = False
        conf_t.requires_grad = False

        pos = conf_t > 0
        pos_num = pos.sum(dim=1, keepdim=True)  # 每张图片匹配到的正样本个数

        # 位置loss: Smooth L1 loss
        pos_idx = pos.unsqueeze(pos.dim()).expand_as(loc_data)
        loc_p = loc_data[pos_idx].view(-1, 4)
        loc_t = loc_t[pos_idx].view(-1, 4)
        loss_l = F.smooth_l1_loss(loc_p, loc_t, reduction="sum")   # shape: [N]

        batch_conf = conf_data.view(-1, self.num_classes)
        loss_c = log_sum_exp(batch_conf) - batch_conf.gather(1, conf_t.view(-1, 1))

        # Hard Negative Mining
        loss_c = loss_c.clone().view(pos.size())
        loss_c[pos] = 0
        # 排序，得到一个索引，它的值表示这个位置的元素第几大
        _, loss_idx = loss_c.sort(1, descending=True)
        _, idx_rank = loss_idx.sort(1)
        num_pos = pos.long().sum(1, keepdim=True)
        num_neg = torch.clamp(self.negpos_ratio * num_pos, max=pos.size(1) - 1)
        neg = idx_rank < num_neg.expand_as(idx_rank)

        # 分类loss
        pos_idx = pos.unsqueeze(2).expand_as(conf_data)
        neg_idx = neg.unsqueeze(2).expand_as(conf_data)
        conf_p = conf_data[(pos_idx + neg_idx).gt(0)].view(-1, self.num_classes)
        targets_weighted = conf_t[(pos + neg).gt(0)]
        loss_c = F.cross_entropy(conf_p, targets_weighted, reduction="sum")

        # N = num_pos.data.sum()
        # loss_l /= N
        # loss_c /= N
        # return loss_l, loss_c
        total_loss = loss_l + loss_c
        num_mask = (pos_num > 0).float()
        pos_num = pos_num.float().clamp(min=1e-6)
        total_loss = (total_loss * num_mask / pos_num).mean(dim=0)
        return total_loss


